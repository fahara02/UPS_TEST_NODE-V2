#ifndef STATE_DEFINES_H_
#define STATE_DEFINES_H_
#include "freertos/FreeRTOS.h"
#include "freertos/event_groups.h"
#include <cstdint>

namespace Node_Core {
using StateBits = uint32_t;

enum class State : StateBits {
  DEVICE_ON = 1 << 0,         // 00000001
  DEVICE_OK = 1 << 1,         // 00000010
  DEVICE_SETUP = 1 << 2,      // 00000100
  DEVICE_READY = 1 << 3,      // 00001000
  MANUAL_MODE = 1 << 4,       // 00010000
  AUTO_MODE = 1 << 5,         // 00100000
  TEST_START = 1 << 6,        // 01000000
  TEST_IN_PROGRESS = 1 << 7,  // 010000000
  CURRENT_TEST_OK = 1 << 8,   // 0100000000
  READY_NEXT_TEST = 1 << 9,   // 01000000000
  RETEST = 1 << 10,           // 010000000000
  MAX_RETEST = 1 << 11,
  ALL_TEST_DONE = 1 << 12,
  SAVE_TEST_DATA = 1 << 13,      // 0010000000000000
  FAILED_TEST = 1 << 14,         // 0100000000000000
  ADDENDUM_TEST_DATA = 1 << 15,  // 1000000000000000
  REPORT_AVAILABLE = 1 << 16,    // 000000010000000000000000
  TRANSPORT_DATA = 1 << 17,      // 000000100000000000000000
  SYSTEM_TUNING = 1 << 18,       // 000001000000000000000000
  FAULT = 1 << 19,               // 000010000000000000000000
};

enum class Event {
  NONE = -1,
  ERROR = 0,
  SELF_CHECK_OK,
  WIFI_CONNECTED,
  WIFI_DISCONNECTED,
  SETTING_LOADED,
  MANUAL_OVERRIDE,
  AUTO_TEST_CMD,
  DATA_CAPTURED,
  VALID_DATA,
  TEST_DONE,
  TEST_LIST_EMPTY,
  DATA_CAPTURE_OK,
  LOAD_BANK_CHECKED,
  VALIDATE_TEST,
  INPUT_OUTPUT_READY,
  INTERRUPT_READY,
  TIMER_READY,
  MESURED_DATA_RECEIVED,
  SAVE,
  DATA,
  MANUAL_DATA_ENTRY,
  TRANSPORT_DATA,
  PRINT_DATA,
  TEST_SUCCESS,
  TEST_FAILED,
  RETEST,
  RETEST_OK,
  RETRY_CONNECT,
  RETRY_OK,
  SYSTEM_FAULT,
  POWER_DEVICE_DOWN,
  SWITCHING_DEVICE_DOWN,
  RESTART
};

}  // namespace Node_Core
#endif